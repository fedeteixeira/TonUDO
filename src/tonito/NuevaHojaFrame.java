    private void panelGuardarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelGuardarMousePressed
        if(taAcordes.getText().isEmpty()||tfNombre.getText().isEmpty()){
            lAsteriscoAcordes.setVisible(true);
            lAsteriscoCamposVacios.setVisible(true);
            lAsteriscoNombre.setVisible(true);
            lCamposVacios.setVisible(true);
            
            
            
        }else{
            Acordes acordes;
            String acord, tonoA, nombre;
            lAsteriscoAcordes.setVisible(false);
            lAsteriscoCamposVacios.setVisible(false);
            lAsteriscoNombre.setVisible(false);
            lCamposVacios.setVisible(false);
            acord = taAcordes.getText();
            tonoA = comboTonoActual.getSelectedItem().toString();
            nombre = tfNombre.getText();
            acordes = new Acordes(acord,tonoA,nombre);
            music.nuevahoja(acordes);
            InicioFrame frameinicio= new InicioFrame(music);
            frameinicio.setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_panelGuardarMousePressed

