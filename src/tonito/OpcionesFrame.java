    private void panelGuardarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelGuardarMousePressed
        if(tfAutor.getText().isEmpty()||tfNombreCancion.getText().isEmpty()){
            
            lAsteriscoModificarAutor.setVisible(true);
            lAsteriscoModificarNombreCancion.setVisible(true);
            lCamposVacios.setForeground(new java.awt.Color(255,0,0));
            
        }else{
            
            lAsteriscoModificarAutor.setVisible(false);
            lAsteriscoModificarNombreCancion.setVisible(false);
            lCamposVacios.setForeground(new java.awt.Color(102,0,0));
            music.setAutor(tfAutor.getText());
            music.setNombre(tfNombreCancion.getText());
            InicioFrame frameinicio= new InicioFrame(music);    
            frameinicio.setVisible(true);
            this.setVisible(false);	
        }
    }//GEN-LAST:event_panelGuardarMousePressed

