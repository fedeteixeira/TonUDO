    private void panelEliminarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelEliminarMousePressed
        
        if(listaHojas.getSelectedIndex()!=-1){
            int respuesta=JOptionPane.showConfirmDialog(null, "Â¿Realmente desea eliminar esta hoja?");
            if(respuesta==0){
                int indice= listaHojas.getSelectedIndex();
                modelo.clear();
                music.eliminarhoja(indice);

                if(music.getContador()>0){
                    for(int i=0; i<music.getContador(); i++){
                        modelo.addElement(music.mostrar(i));
                        listaHojas.setModel(modelo);
                        listaHojas.ensureIndexIsVisible(modelo.getSize());
                    }
                }
                lTransportar.setForeground(new  java.awt.Color(102,102,102));
                lEditar.setForeground(new  java.awt.Color(102,102,102));
                lEliminar.setForeground(new  java.awt.Color(102,102,102));
            }
        }
        
    }//GEN-LAST:event_panelEliminarMousePressed

